// Opening-scene NPC sprites — raw 2BPP tile bytes captured from FF3 NES OAM
// at frame 1860 (the scene a new player spawns into on map 7). These tiles
// are loaded into PPU sprite RAM by a special CHR bank-switch during the
// cutscene; they don't appear in any of the standard player/NPC GFX banks,
// so we render them from the inline bytes here rather than via Sprite.setGfxID.
//
// Per-NPC fields:
//   tl/tr/bl/br — four 8x8 tile patterns (16 bytes each, planar 2BPP)
//   palTop / palBtm — NES palette indices for top + bottom halves
//   flipAll  — render with full HFLIP (for the left attendant facing right)
//   flipBtm  — render bottom row HFLIPped only (FF3 NES "DOWN frame 1" pose)
//
// PPU palette state at capture (sprite palettes 2 + 3):
//   SP2 = [0x1A, 0x0F, 0x12, 0x36]
//   SP3 = [0x1A, 0x0F, 0x27, 0x30]

const SCENE_PAL_TOP = [0x1A, 0x0F, 0x27, 0x30]; // pal3
const SCENE_PAL_BTM = [0x1A, 0x0F, 0x12, 0x36]; // pal2

