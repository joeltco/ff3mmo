// Revive angel — the little spirit that flaps beside a downed character in
// FF3's party-death scene. Captured from FF3 NES OAM (REC OAM @ f5299): a 16×16
// (2×2 tile) sprite, SP3 palette, 3-frame wing flap.
//
// Used by the FenixDown auto-revive (battle-fenix-revive.js): it appears to the
// LEFT of the death pose and flaps while the character is brought back.
//
// Tile bytes are the captured CHR (NES 2bpp planar, 8 bytes plane0 + 8 plane1).
// Do NOT hand-edit — recapture via the EMU OAM dumper if the source changes.

import { _make8Canvas } from '../canvas-utils.js';

// SP3 palette from the capture: transparent / tan / pink / white.
const ANGEL_PAL = [0x0F, 0x27, 0x36, 0x30];

// 3 flap frames, each 2×2 tiles in [TL, TR, BL, BR] order.
const FRAME_A = [
  new Uint8Array([0x00,0x00,0x00,0x0F,0x77,0xFF,0x1F,0x3F,0x00,0x01,0x00,0x0C,0x70,0xF8,0x18,0x38]), // $4D
  new Uint8Array([0x7C,0x02,0x3C,0x02,0xFF,0xFB,0x91,0x02,0xFC,0x02,0xFC,0x00,0x00,0x04,0x6C,0x90]), // $4E
  new Uint8Array([0x0E,0x00,0x1C,0x3C,0x3E,0x04,0x00,0x00,0x0D,0x01,0x1C,0x3F,0x3E,0x1D,0x7D,0xFB]), // $4F
  new Uint8Array([0x02,0x02,0x94,0x10,0x00,0x00,0x00,0x00,0x58,0xD9,0x69,0x2A,0xC4,0xF8,0xC0,0x80]), // $50
];
const FRAME_B = [
  new Uint8Array([0x00,0x01,0x00,0x03,0x07,0x07,0x07,0x07,0x00,0x01,0x00,0x00,0x00,0x00,0x00,0x00]), // $51
  new Uint8Array([0xFC,0x00,0xE0,0x02,0xFF,0xFB,0x91,0x02,0xFC,0x02,0xFC,0x00,0x00,0x04,0x6C,0x90]), // $52
  new Uint8Array([0x7E,0xF0,0xAC,0x1C,0x3E,0x04,0x00,0x00,0x7D,0xF1,0xAC,0x1F,0x3E,0x1D,0x7D,0xFB]), // $53
  new Uint8Array([0x02,0x02,0x84,0x00,0x04,0x08,0x00,0x00,0x58,0xD8,0x78,0x3A,0xC3,0xF7,0xC0,0x80]), // $54
];
const FRAME_C = [
  new Uint8Array([0x00,0x01,0x00,0x0F,0x77,0xFF,0x1F,0x3F,0x00,0x01,0x00,0x0C,0x70,0xF8,0x18,0x38]), // $49
  new Uint8Array([0x0C,0x02,0xFC,0x02,0xFF,0xFB,0x91,0x02,0xFC,0x02,0xFC,0x00,0x00,0x04,0x6C,0x90]), // $4A
  new Uint8Array([0x0E,0x00,0x1C,0x3C,0x3E,0x04,0x00,0x00,0x0D,0x01,0x1C,0x3F,0x3E,0x1D,0x7D,0xFB]), // $4B
  new Uint8Array([0x02,0x02,0x84,0x00,0x00,0x00,0x00,0x00,0x58,0xD8,0xFC,0x38,0xC4,0xFE,0xCE,0x87]), // $4C
];

let _frames = null;

function _compose(tiles) {
  const c = document.createElement('canvas');
  c.width = 16; c.height = 16;
  const cx = c.getContext('2d');
  cx.drawImage(_make8Canvas(tiles[0], ANGEL_PAL), 0, 0);
  cx.drawImage(_make8Canvas(tiles[1], ANGEL_PAL), 8, 0);
  cx.drawImage(_make8Canvas(tiles[2], ANGEL_PAL), 0, 8);
  cx.drawImage(_make8Canvas(tiles[3], ANGEL_PAL), 8, 8);
  return c;
}

// Lazily build (needs a DOM canvas). Returns [frameA, frameB, frameC] — 16×16.
export function getReviveAngelFrames() {
  if (!_frames) _frames = [_compose(FRAME_A), _compose(FRAME_B), _compose(FRAME_C)];
  return _frames;
}
