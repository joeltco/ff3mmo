// Tile draw positions inside the 16×32 canvas (matches the OAM layout).
const POS = [[0, 0], [8, 0], [0, 8], [8, 8], [0, 16], [8, 16], [0, 24], [8, 24]];

function _drawTile(cctx, nesColors, bytes, dx, dy) {
  const px = decodeTile(bytes, 0);
  const img = cctx.createImageData(8, 8);
  for (let p = 0; p < 64; p++) {
    const ci = px[p];
    const i = p * 4;
    if (ci === 0) { img.data[i + 3] = 0; continue; }   // color 0 = transparent
    const [r, g, b] = nesColors[ci] || [0, 0, 0];
    img.data[i] = r; img.data[i + 1] = g; img.data[i + 2] = b; img.data[i + 3] = 255;
  }
  cctx.putImageData(img, dx, dy);
}

function _buildFrame(tiles, nc) {
  const c = document.createElement('canvas');
  c.width = 16; c.height = 32;
  const x = c.getContext('2d');
  for (let t = 0; t < 8; t++) _drawTile(x, nc, tiles[t], POS[t][0], POS[t][1]);
  return c;
}

// Returns [frameA, frameB, frameC] — each a 16×32 canvas. Index 0 of the
// palette is unused (transparent); 1/2/3 = outline / blue / pale blue.
export function initCrystalSprite() {
  const nc = [0x1A, 0x0F, 0x11, 0x31].map(c => NES_SYSTEM_PALETTE[c] || [0, 0, 0]);
  return [_buildFrame(FRAME_A, nc), _buildFrame(FRAME_B, nc), _buildFrame(FRAME_C, nc)];
}
